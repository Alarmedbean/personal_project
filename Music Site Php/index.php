<p>Aubrey Drake Graham (born October 24, 1986)[5] is a Canadian rapper, singer, songwriter, record producer, actor, and entrepreneur.[6] Drake initially gained recognition as an actor on the teen drama television series Degrassi: The Next Generation in the early 2000s. Intent on pursuing a career as a rapper, he departed the series in 2007 following the release of his debut mixtape, Room for Improvement. He released two further independent projects, Comeback Season and So Far Gone, before signing to Lil Wayne's Young Money Entertainment in June 2009.[7]</p>

<p>Drake released his debut studio album Thank Me Later in 2010,[8] which debuted at number one on the US Billboard 200 and was soon certified platinum. His next two releases, 2011's Take Care and 2013's Nothing Was the Same,[9][10] were critically and commercially successful; the former earned him his first Grammy Award for Best Rap Album. In 2015, he released two mixtapes—the trap-influenced If You're Reading This It's Too Late and a collaboration with Future titled What a Time to Be Alive—both of which earned platinum certification in the U.S.[11]</p>

<p>His fourth album, Views (2016), broke several chart records.[12] The dancehall-influenced album sat atop the Billboard 200 for 13 nonconsecutive weeks,[13] becoming the first album by a male solo artist to do so in over 10 years. The album's second single, "One Dance", topped the charts in several countries, and became his first number-one single as a lead artist. That year, Drake led both the Billboard Hot 100 and the Billboard 200 charts simultaneously for eight weeks.[14] Views achieved quadruple platinum status in the US, and earned over 1 million album-equivalent units in the first week of its release.[15][16] Its lead single "Hotline Bling" received Grammy Awards for Best Rap/Sung Performance and Best Rap Song.[17] In 2017, he released the mixtape More Life. Described by Drake as a "playlist",[18][19] it became his seventh consecutive number one on the Billboard 200, and set multiple streaming records.[20] A year later, he released the double album Scorpion, which also broke several streaming records,[21] and housed the number-one singles "God's Plan", and the bounce-infused "Nice for What" and "In My Feelings".</p>

<p>Drake holds several Billboard chart records. He has the most charted songs (186) among solo artists in the history of the Billboard Hot 100, the most simultaneously charted Hot 100 songs in a single week, the most time on the Hot 100 (431 weeks) and the most Hot 100 debuts in a week. He also has the most number one singles on the Hot Rap Songs, Hot R&B/Hip-Hop Airplay and Rhythmic Charts.[23][24] Drake has also won three Juno Awards, six American Music Awards, and fifteen Billboard Music Awards. He is the RIAA's top certified digital singles artist and one of the world's best-selling music artists, with more than 10 million albums and 86 million singles sold globally.</p>

<p>As an entrepreneur, Drake has founded the OVO Sound record label with longtime collaborator 40, as well as using the "OVO" moniker to create a clothing line and program on Beats 1 Radio.</p>
